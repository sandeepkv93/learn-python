# int
num1: int = 10
num2: int = 20
print(num1 + num2)  # 30

# float
num3: float = 10.5
num4: float = 20.5
print(num3 + num4)  # 31.0

# str
str1: str = "Hello"
str2: str = "World"
print(str1 + str2)  # HelloWorld

# bool
bool1: bool = True
bool2: bool = False
print(bool1 and bool2)  # False

# list
list1: list = [1, 2, 3]
list2: list = [4, 5, 6]
print(list1 + list2)  # [1, 2, 3, 4, 5, 6]

# tuple
tuple1: tuple = (1, 2, 3)
tuple2: tuple = (4, 5, 6)
print(tuple1 + tuple2)  # (1, 2, 3, 4, 5, 6)

# dict
dict1: dict = {"a": 1, "b": 2}
dict2: dict = {"c": 3, "d": 4}
print(dict1 | dict2)  # {'a': 1, 'b': 2, 'c': 3, 'd': 4}

# set
set1: set = {1, 2, 3}
set2: set = {4, 5, 6}
print(set1 | set2)  # {1, 2, 3, 4, 5, 6}
